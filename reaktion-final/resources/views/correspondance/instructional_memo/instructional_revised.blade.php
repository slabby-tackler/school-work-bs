	<strong>Subject:</strong> Proper Way to Hunt Fox
</p>

<p>
	It has come to the attention of the club that some members have been foot hunting foxes. Several years ago, this club decided that it was unsportsmanlike to hunt in this manner, and a
	decision was made to ban it. For those who are unfamiliar with this practice, it is having hounds chase a fox into a line of men with loaded guns.
</p>

<p>
	This club only supports the traditional way of hunting fox. As a reminder, here is how the traditional hunt is done:
</p>

<p>
	<ol>
		<li class="inquiry-number">
			Gather your horses, horsemen, hounds, and poles. The pole is not used as a weapon to kill the fox. It is best to have more than two hounds to prevent them from getting seriously
			injured.
		</li>
		<li class="inquiry-number">
			Allow your hounds to sniff out the den of a fox in the area.
		</li>
		<li class="inquiry-number">
			Once at the den use the pole to scare the fox out, and allow the hounds to give chase. When the chase begins follow behind on horseback.
		</li>
		<li class="inquiry-number">
			The hounds should be able to handle the rest. Eventually the fox will tire, and the hounds will begin to take down the fox.
		</li>
		<li class="inquiry-number">
			When the hounds finish with their work, your bounty will be lying on the ground
		</li>
	</ol>
</p>

<p>
	For any questions regarding the hunting foxes in this club contact me at nwade3@uncc.edu. Happy hunting.
</p>
